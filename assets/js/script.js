class contact{
    constructor(name, landline, phone, imgLink, date, email, cep, city, insta, gitHub){
        this.name = name;
        this.landline = landline;
        this.phone = phone;
        this.imgLink = imgLink;
        this.date = date;
        this.email = email;
        this.cep = cep;
        this.city = city;
        this.insta = insta;
        this.gitHub = gitHub;
    }
}